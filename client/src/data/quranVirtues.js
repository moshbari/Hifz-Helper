// Get a random quote
export const getRandomQuote = () => {
  const randomIndex = Math.floor(Math.random() * quranVirtuesData.length);
  return quranVirtuesData[randomIndex];
};

// Get a random quote by category
export const getRandomQuoteByCategory = (category) => {
  const filtered = quranVirtuesData.filter(q => q.category === category);
  if (filtered.length === 0) return getRandomQuote();
  const randomIndex = Math.floor(Math.random() * filtered.length);
  return filtered[randomIndex];
};

// Get multiple random quotes
export const getRandomQuotes = (count = 3) => {
  const shuffled = [...quranVirtuesData].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

